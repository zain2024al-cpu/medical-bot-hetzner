def get_daily_term() -> tuple[str, str, str]:
    """
    Return today's medical term as (arabic_term, english_term, arabic_definition).
    Rotates daily using day-of-year index, cycling through all 60 terms.
    """
    day_index = datetime.utcnow().timetuple().tm_yday - 1   # 0-based (0–364)
    return _TERMS[day_index % len(_TERMS)]
